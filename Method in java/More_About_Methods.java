public class More_About_Methods {

    public static void greet(){
        System.out.println("Hii , Good Morning !");
    }

    public static void num() {
        for (int i = 0; i <= 10; i++) {
            System.out.print(i + " ");
        }
    }
    public static void main(String[] args) {
        greet();
        num();
    }
    
}
